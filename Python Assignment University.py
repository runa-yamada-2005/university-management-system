def assigned_modules():
    print("\n --- Assigned Modules ---")
    print("Let me check your assigned modules...")
    lecturer_id = input("Enter your lecturer ID: ").strip() #Get lecturer ID

    try:
        #Read the file: "r" mode
        with open("lecturer_modules.txt", "r") as file:
            modules_found = False #Flag to check if modules were found for the given lecturer ID

            # Check each line in the file
            for line in file:
                data = line.strip().split(",") #to make a list, split a line with a comma

                #Check the Lecturer ID
                if data[0] == lecturer_id:  #If there is an existence of Lecturer
                    print(f"Recognizing you as {data[1]} !")
                    print(f"This is modules assigned to Lecturer {lecturer_id}:")
                    print(f"- Course Name: {data[2]}\n"
                          f"- Module code: {data[3]}\n"
                          f"- Module Name: {data[4]}\n"
                          f"- Credit Hour: {data[5]}")
                    modules_found = True #True = modules are found
                    break

            #If no modules were found for the lecturer ID, print this
            if not modules_found:
                print(f"Error: No modules found for Lecturer ID {lecturer_id}.")
                print("Please check the ID again or contact the admin office.")

    except FileNotFoundError:
        print("Error: The file for assigned modules does not exist.")

    #Ask user for further action
    response = input("Would you like to do something else? (Y/N): ").strip().lower()
