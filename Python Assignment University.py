def verification():
    data = []
    with open("login.txt", 'r') as sourcefile:
        for row in sourcefile:
            rows = row.strip().split(",")
            data.append(rows)
    input_username = input("Enter username.(e,g:Amelia)")
    for record in data:
        if input_username in record:
            input_password = input("Enter password.(e.g:amelia_123)")
            username = record[0]
            password = record[1]
            if input_username == username:
                if input_password == password:
                    print("Login successful.")
                    return True

    print("Invalid username or password.")
    return False

